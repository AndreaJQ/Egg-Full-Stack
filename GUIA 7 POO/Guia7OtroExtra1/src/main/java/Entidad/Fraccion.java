
package Entidad;

public class Fraccion {
    //atributos
    private int f1Denom;
    private int f1Num;
    private int f2Denom;
    private int f2Num;

    public Fraccion() {
    }

    public Fraccion(int f1Denom, int f1Num, int f2Denom, int f2Num) {
        this.f1Denom = f1Denom;
        this.f1Num = f1Num;
        this.f2Denom = f2Denom;
        this.f2Num = f2Num;
    }

    public int getF1Denom() {
        return f1Denom;
    }

    public void setF1Denom(int f1Denom) {
        this.f1Denom = f1Denom;
    }

    public int getF1Num() {
        return f1Num;
    }

    public void setF1Num(int f1Num) {
        this.f1Num = f1Num;
    }

    public int getF2Denom() {
        return f2Denom;
    }

    public void setF2Denom(int f2Denom) {
        this.f2Denom = f2Denom;
    }

    public int getF2Num() {
        return f2Num;
    }

    public void setF2Num(int f2Num) {
        this.f2Num = f2Num;
    }
    
    
    
    
    
    
}
